compParalelepipedo = float(input("Insira o comprimento do paralelepipedo: "))
largParalelepipedo = float(input("Insira a largura do paralelepipedo: "))
altParalelepipedo = float(input("Insira a altura do paralelepipedo: "))

volParalelepipedo = compParalelepipedo * largParalelepipedo * altParalelepipedo

print("Volume do paralelepipedo: ", volParalelepipedo)